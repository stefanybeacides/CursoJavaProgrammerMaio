package entidade;

public abstract class Animal {
	
	private String nome;
	private String caf;
	
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getCaf() {
		return caf;
	}
	
	public void setCaf(String caf) {
		this.caf = caf;
	}

}
